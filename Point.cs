namespace lw1;

public class Point
{
    private int x;
    private int y;

    public Point(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int coord1
    {
        get
        {
            return x;
        }
    }

    public int coord2
    {
        get
        {
            return y;
        }
    }
}